     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $cliente = new Cliente();
        $cliente->clienteNombre = $request->name;
        $cliente->clienteTelefono = $request->tel;
        $cliente->clienteEmail = $request->email;
        $cliente->clienteRazon = $request->razon;
        $cliente->clienteLimit = $request->limiteCredito;
        $cliente->clienteDomicilio = $request->domicilio;

        return response()->json([
            'status' => $cliente->save()
        ]);
    }

